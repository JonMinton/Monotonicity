    <p class="MsoNormal" style="line-height: 200%">
      If the decision to use a method which ensures monotonicity between variables is made, then the most appropriate methods appear to be either to be a covariance-based method or the difference method. As described in the findings section above, of these two approaches, the difference method has a number of advantages, and we have produced an easy-to-use Excel workbook to facilitate its use.
