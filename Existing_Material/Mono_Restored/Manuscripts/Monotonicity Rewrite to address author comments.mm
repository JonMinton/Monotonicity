  <body>
    <p class="MsoNormal" style="line-height: 200%">
      Some of the methods for ensuring monotonicity should be avoided for statistical reasons. These are the replacement methods (three and four) and the resampling methods (five and six). These produce biased estimates of the means and variances of the distributions, shifting the centres of these distributions by introducing discontinuities. They produce estimates which do not adequately represent the summary information used in their construction. We warn against the use of these methods even where there is strong belief in the clinical relationship between variables. As described above, there are theoretical reasons to be cautious about the use of methods seven to nine, although in practice estimates produced by these methods may be reasonable.
    </p>
  </body>
