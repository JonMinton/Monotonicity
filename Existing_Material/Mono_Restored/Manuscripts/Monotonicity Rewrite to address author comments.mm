    <div class="WordSection1">
      <p style="line-height: 200%" class="MsoNormal">
        Many medical conditions have different levels of severity. In modelling these conditions, it is often assumed by clinicians and decision analytic modellers that greater severity will mean poorer health, greater treatment cost, or both. This is an example of a monotonicity assumption. If there are strong clinical reasons to believe it, then even when there is uncertainty about the mean values associated with different severity levels, there is still an expectation that more severe will on average be worse than less severe. To incorporate the expectation that estimates associated with different states should follow a known order, estimates produced by independent sampling may be inadequate, and alternative methods which formally incorporate this ordering assumption may be needed.
      </p>
    </div>
