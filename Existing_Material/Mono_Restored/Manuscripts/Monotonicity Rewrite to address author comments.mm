<node COLOR="#111111" CREATED="1397744490816" ID="ID_1862821043" MODIFIED="1397855929776">
<richcontent TYPE="NODE"><html>
  <head>
    
  </head>
  <body>
    <p class="MsoNormal" style="line-height: 200%">
      A third limitation relates to how methods eight and nine have been implemented. Both of these involve choosing covariance parameters conditional of whether any pair of values in a &#8216;training&#8217; sample of 1,000 draws violates the monotonicity assumption. As the size of the &#8216;training&#8217; sample increases, the probability of extreme values, including values which violate monotonicity, increases, and so we should expect the covariance selected to depend partly on the size of the training sample used.
    </p>
    <p class="MsoNormal" style="line-height: 200%">
      The final limitation relates to the existence of worse-than-death health states exist where negative values are recorded and where it would be inappropriate to use Beta distributions bounded within the range 0 to 1. This problem could be addressed by rescaling the output from the Beta distributions from the range 0 to 1 to the range -0.594 to 1, for example, if representing HRQOLHRQOL scores using the EuroQol Quality of Life (EQ-5D) scale. (5,6)
    </p>
