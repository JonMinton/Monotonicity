    <p class="MsoNormal" style="line-height: 200%">
      The decision to use a method which forces monotonicity needs to be based on good clinical evidence and in consultation with clinical experts. If there is neither strong evidence nor a consensus of clinical opinion that variables have this monotonic relationship, then simpler methods such as independent sampling, in which monotonicity can be violated, may be more appropriate.
    </p>
