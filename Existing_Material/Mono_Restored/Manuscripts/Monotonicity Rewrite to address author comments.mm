    <p class="MsoNormal" style="line-height: 200%">
      A second limitation, related to the first limitation, is that our hypothetical dataset have only two disease severity states, U<sub>1</sub>&#160;and U<sub>2</sub>, rather than three states such as U<sub>0</sub>, U<sub>1</sub>, and U<sub>2</sub>, where the monotonicity condition becomes &#160;and . Introducing further states would lead to additional complexity for methods seven, eight and nine, for example, as we would have to estimate three covariances (i.e. cov(U<sub>0</sub>, U<sub>2</sub>), cov(U<sub>0</sub>, U<sub>1</sub>) and cov(U<sub>1</sub>,U<sub>2</sub>) ) rather than just one. This provides further justification for favouring method ten.
    </p>
